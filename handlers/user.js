'use strict';
var dataProvider = require('../data/user.js');
/**
 * Operations on /user
 */
module.exports = {
  /**
   * summary:
   * description: Get the authenticated user.
   * parameters: X-GitHub-Media-Type, Accept, X-RateLimit-Limit, X-RateLimit-Remaining, X-RateLimit-Reset, X-GitHub-Request-Id
   * produces:
   * responses: 200, 403
   */
  get: function (req, res, next) {
    /**
     * Get the data for response 200
     * For response `default` status 200 is used.
     */
    var status = 200;
    var provider = dataProvider['get']['200'];
    provider(req, res, function (err, data) {
      if (err) {
        next(err);
        return;
      }
      res.status(status).send(data && data.responses);
    });
  },
  /**
   * summary:
   * description: Update the authenticated user.
   * parameters: X-GitHub-Media-Type, Accept, X-RateLimit-Limit, X-RateLimit-Remaining, X-RateLimit-Reset, X-GitHub-Request-Id, body
   * produces:
   * responses: 200, 403
   */
  patch: function (req, res, next) {
    /**
     * Get the data for response 200
     * For response `default` status 200 is used.
     */
    var status = 200;
    var provider = dataProvider['patch']['200'];
    provider(req, res, function (err, data) {
      if (err) {
        next(err);
        return;
      }
      res.status(status).send(data && data.responses);
    });
  }
};
